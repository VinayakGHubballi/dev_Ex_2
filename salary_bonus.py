# salary_bonus.py

salary = float(input("Enter your salary: "))

bonus = salary * 0.10
total_salary = salary + bonus

tax = total_salary * 0.05
after_tax = total_salary - tax

print("Original Salary:", salary)
print("Bonus Added:", total_salary)
print("After Tax Deduction:", after_tax)
